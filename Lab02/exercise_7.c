#include <stdio.h>
int main(){
    int width = 500;
    int height = 300;
    int sum;
    sum = 500 * 300;
    printf("The perimeter of a rectangle is the sum of its four sides.\nIt is calculated using the formula, Perimeter of Rectangle = 2 (Length + Width). The area of a rectangle is the region covered within its boundary and this can be calculated using the formula, area of rectangle = length * width.\n\n");
    printf("Area of rectangle = %d * %d = %d square metre", width, height, sum);
    return 0;

}